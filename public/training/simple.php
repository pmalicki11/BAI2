    <h1>Polonez</h1>
    <p class="par1"><b>Polonez</b> (właśc. <i>Pomylonez</i>, ewentualnie <i>Borewicz</i>, z wł. <i>Polacco Nuova</i>) – coś, co powstało w 1977 u Włochów na zlecenie Towarzysza Stalina tow. Gierka. Miał być to pojazd łączący funkcjonalność, bezpieczeństwo oraz wygodę niestety się nie udało. Ponieważ projekt upadł, polska firma OSF przejęła pracę nad tym pojazdem. W 1978 roku Polonez trafił do seryjnej produkcji. Pojazdy te cieszyły się sporym poparciem Waldiego. W latach 90. Polonezy z odzysku zostały wykorzystane do teleturnieju Koło Fortuny.</p>
